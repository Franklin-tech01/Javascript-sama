// common string methods

let email = 'franklinmike2021@gmail.com';

//1) lastindexOf
let result = email.lastIndexOf('l');
console.log(result);

//2)slice
result = email.slice(0, 10);
console.log(result);

//3)substr(substring)
result = email.substr(4, 10);
console.log(result);

// 4)replace
result = email.replace('f', 'p');
console.log(result);

