// switch statement

const grade = 3;

switch (grade) {
    case 'a':
        console.log("You got an a")
        break;
    case 'b':
        console.log("You got a b")
        break;
    case 'c':
        console.log("You got a c")
        break;
    case 'd':
        console.log("You got a d")
        break;
    case 'e':
        console.log("You got a e")
        break;

    default:
        console.log("Not a vaid grade")
        break;
}